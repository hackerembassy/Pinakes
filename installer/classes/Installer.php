    /**
     * Create admin user
     */
    public function createAdminUser($nome, $cognome, $email, $password) {

        $pdo = $this->getDatabaseConnection();

        // Decode HTML entities that might have been added during sanitization
        $nome = html_entity_decode($nome, ENT_QUOTES, 'UTF-8');
        $cognome = html_entity_decode($cognome, ENT_QUOTES, 'UTF-8');

        // Generate unique codice_tessera
        $codiceTessera = 'ADMIN-' . date('Ymd') . '-' . str_pad((string)rand(1, 999), 3, '0', STR_PAD_LEFT);

        // Hash password
        $passwordHash = password_hash($password, PASSWORD_BCRYPT, ['cost' => 12]);

        // Resolve the locale chosen during the installer wizard. Without
        // this the new admin row falls through to the schema default for
        // utenti.locale ('it_IT'), and AuthController on first login then
        // calls I18n::setLocale('it_IT') from $row['locale'], reverting
        // the language no matter what the user picked at step 0 (#112).
        $sessionLocale = $_SESSION['app_locale'] ?? '';
        $envLocale     = $this->config['APP_LOCALE'] ?? '';
        $rawLocale     = trim((string) ($sessionLocale !== '' ? $sessionLocale : $envLocale));
        $localeMap = [
            'it' => 'it_IT', 'it_it' => 'it_IT',
            'en' => 'en_US', 'en_us' => 'en_US',
            'de' => 'de_DE', 'de_de' => 'de_DE',
            'fr' => 'fr_FR', 'fr_fr' => 'fr_FR',
        ];
        $supportedLocales = array_values(array_unique(array_values($localeMap)));
        $normalized = strtolower(str_replace('-', '_', $rawLocale));
        if (isset($localeMap[$normalized])) {
            $adminLocale = $localeMap[$normalized];
        } elseif (in_array($rawLocale, $supportedLocales, true)) {
            $adminLocale = $rawLocale;
        } else {
            $adminLocale = 'it_IT';
        }

        // Insert admin user
        $query = "
            INSERT INTO utenti (
                nome, cognome, email, password, codice_tessera,
                tipo_utente, stato, email_verificata, locale,
                data_scadenza_tessera, created_at, updated_at
            ) VALUES (
                :nome, :cognome, :email, :password, :codice_tessera,
                'admin', 'attivo', 1, :locale,
                DATE_ADD(NOW(), INTERVAL 10 YEAR), NOW(), NOW()
            )
        ";

        try {
            $stmt = $pdo->prepare($query);

            $stmt->execute([
                'nome' => $nome,
                'cognome' => $cognome,
                'email' => $email,
                'password' => $passwordHash,
                'codice_tessera' => $codiceTessera,
                'locale' => $adminLocale,
            ]);

            $userId = $pdo->lastInsertId();

            return [
                'id' => $userId,
                'codice_tessera' => $codiceTessera
            ];
        } catch (PDOException $e) {
            // A duplicate email means an admin with this address already exists —
            // e.g. re-running the installer against a database that survived while
            // the .installed marker was lost (failed-install retry, DB restore
            // without the marker, or a docroot/marker mishap). Surface a clear,
            // actionable message instead of a raw SQL error.
            if ($e->getCode() === '23000' || stripos($e->getMessage(), 'Duplicate entry') !== false) {
                throw new \RuntimeException(sprintf(
                    __("Esiste già un utente con l'email %s. Se stai reinstallando su un database esistente, accedi con le credenziali esistenti oppure usa un'altra email."),
                    $email
                ), 0, $e);
            }
            throw $e;
        }
    }

