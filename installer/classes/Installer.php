    /**
     * Import data using PDO (works on all hosting)
     */
    private function importDataViaPDO($dataFile) {
        // Read data file
        $sql = file_get_contents($dataFile);
        if ($sql === false) {
            throw new Exception("Impossibile leggere il file data.sql");
        }

        // Get PDO connection
        $pdo = $this->getDatabaseConnection();

        // Split SQL into statements (simpler than schema - just INSERT statements)
        $statements = array_filter(explode(";\n", $sql), function($stmt) {
            return !empty(trim($stmt));
        });

        $executedCount = 0;
        $errors = [];

        try {
            $pdo->beginTransaction();
            $pdo->exec('SET FOREIGN_KEY_CHECKS=0');
            foreach ($statements as $statement) {
                $statement = trim($statement);

                if (empty($statement)) {
                    continue;
                }

                try {
                    $pdo->exec($statement);
                    $executedCount++;
                } catch (PDOException $e) {
                    // Collect errors but continue (some INSERTs might fail due to duplicates)
                    if (strpos($e->getMessage(), 'Duplicate entry') === false) {
                        $errors[] = "Data import error: " . $e->getMessage();
                    }
                }
            }

            if (!empty($errors)) {
                throw new Exception(sprintf(__("Errori durante l'import dei dati (%d). Primi errori:\n%s"), count($errors), implode("\n", array_slice($errors, 0, 5))));
            }

            $pdo->commit();
        } catch (\Throwable $e) {
            if ($pdo->inTransaction()) {
                $pdo->rollBack();
            }
            throw $e;
        } finally {
            // Always restore FK checks even if execution aborts early
            try {
                $pdo->exec('SET FOREIGN_KEY_CHECKS=1');
            } catch (PDOException $e) {
                error_log('[Installer] Failed to restore FOREIGN_KEY_CHECKS=1: ' . $e->getMessage());
            }
        }

        return true;
    }

    /**
     * Verify schema was imported correctly
     */
    private function verifySchemaImport() {
        // IMPORTANT: Close old PDO connection and reset it
        // This ensures new connections see the newly created tables
        $this->pdo = null;

        // Verify that tables were created with fresh connection
        try {
            $pdo = $this->getDatabaseConnection();
            $stmt = $pdo->query("SHOW TABLES");
            $tables = $stmt->fetchAll(PDO::FETCH_COLUMN);

            if (!is_array($tables) || empty($tables)) {
                throw new Exception("Schema non importato correttamente. Nessuna tabella trovata.");
            }

            $actual = array_map('strtolower', $tables);
            sort($actual);

            $expected = self::EXPECTED_TABLES;
            sort($expected);

            $missing = array_diff($expected, $actual);
            if (!empty($missing)) {
                throw new Exception(sprintf(__("Schema non importato correttamente. Tabelle mancanti: %s"), implode(', ', $missing)));
            }

            if (count($actual) < count($expected)) {
                throw new Exception(sprintf(__("Schema incompleto: trovate %d tabelle su %d attese."), count($actual), count($expected)));
            }
        } catch (PDOException $e) {
            throw new Exception(sprintf(__("Errore verifica tabelle: %s"), $e->getMessage()));
        }

        return true;
    }

    /**
     * Import database triggers
     */
    public function importTriggers() {
        $this->triggerWarnings = [];
        // Use absolute path to avoid issues with chdir()
        $installerDir = dirname(__DIR__);
        $triggersFile = $installerDir . '/database/triggers.sql';

        if (!file_exists($triggersFile)) {
            $this->triggerWarnings[] = "File triggers.sql non trovato — nessun trigger installato.";
            return true;
        }

        $sql = file_get_contents($triggersFile);

        if (!is_string($sql) || trim($sql) === '') {
            $this->triggerWarnings[] = "File triggers.sql vuoto — nessun trigger installato.";
            return true;
        }

        $pdo = $this->getDatabaseConnection();

        // Clean up DEFINER clauses and remove DELIMITER statements
        $sql = preg_replace('/CREATE\s+DEFINER=`[^`]+`@`[^`]+`\s+TRIGGER/i', 'CREATE TRIGGER', $sql);
        $sql = preg_replace('/DELIMITER\s+\S+/i', '', $sql);

        // Split the file into statements using '$$' as the delimiter
        $statements = explode('$$', $sql);

        $executedTriggers = 0;
        foreach ($statements as $statement) {
            $statement = trim($statement);
            if (empty($statement)) {
                continue;
            }

            try {
                $pdo->exec($statement);
                if (stripos($statement, 'CREATE TRIGGER') === 0) {
                    $executedTriggers++;
                }
            } catch (PDOException $e) {
                $message = $e->getMessage();
                $this->triggerWarnings[] = "Impossibile eseguire uno statement del trigger: {$message}";
                // Continue to the next statement
            }
        }

        if ($executedTriggers === 0 && empty($this->triggerWarnings)) {
            $this->triggerWarnings[] = "Nessun trigger valido trovato nel file triggers.sql.";
        }

        return true;
    }

    /**
     * Retrieve warnings generated during trigger import
     *
     * @return string[]
     */
    public function getTriggerWarnings(): array
    {
        return $this->triggerWarnings;
    }

    /**
     * Import optimization indexes
     * These indexes improve query performance but are not critical for basic functionality
     * Compatible with both MySQL and MariaDB
     *
     * @return bool
     */
    public function importOptimizationIndexes(): bool
    {
        $pdo = $this->getDatabaseConnection();

        // Define indexes to create (table => [index_name => columns])
        $indexes = [
            'libri' => [
                'idx_created_at' => 'created_at',
                'idx_isbn10' => 'isbn10',
                'idx_genere_scaffale' => 'genere_id, scaffale_id',
                'idx_sottogenere_scaffale' => 'sottogenere_id, scaffale_id',
                'idx_libri_tipo_media_deleted_at' => 'deleted_at, tipo_media',
            ],
            'libri_autori' => [
                'idx_libro_autore' => 'libro_id, autore_id',
                'idx_autore_libro' => 'autore_id, libro_id',
                'idx_ordine_credito' => 'ordine_credito',
                'idx_ruolo' => 'ruolo',
            ],
            'autori' => [
                'idx_nome' => 'nome(100)',
            ],
            'editori' => [
                'idx_nome' => 'nome(100)',
            ],
            'prestiti' => [
                'idx_stato_attivo' => 'stato, attivo',
                'idx_data_prestito' => 'data_prestito',
                'idx_copia_id' => 'copia_id',
                'idx_origine' => 'origine',
                'idx_libro_utente' => 'libro_id, utente_id',
            ],
            'utenti' => [
                'idx_nome' => 'nome(50)',
                'idx_cognome' => 'cognome(50)',
                'idx_nome_cognome' => 'nome(50), cognome(50)',
                'idx_tipo_utente' => 'tipo_utente',
            ],
            'generi' => [
                'idx_nome' => 'nome(50)',
            ],
            'posizioni' => [
                'idx_scaffale_mensola' => 'scaffale_id, mensola_id',
            ],
            'copie' => [
                'idx_numero_inventario' => 'numero_inventario',
                'idx_sede_id' => 'sede_id',
            ],
            'prenotazioni' => [
                'idx_libro_id' => 'libro_id',
                'idx_utente_id' => 'utente_id',
                'idx_stato' => 'stato',
                'idx_stato_libro' => 'stato, libro_id',
                'idx_queue_position' => 'queue_position',
                'idx_data_scadenza' => 'data_scadenza_prenotazione',
            ],
        ];

        foreach ($indexes as $table => $tableIndexes) {
            // Check if table exists
            try {
                $stmt = $pdo->query("SHOW TABLES LIKE " . $pdo->quote($table));
                if ($stmt->rowCount() === 0) {
                    continue; // Table doesn't exist, skip
                }
            } catch (\PDOException $e) {
                continue;
            }

            // Get existing indexes for this table
            $existingIndexes = [];
            try {
                $stmt = $pdo->query("SHOW INDEX FROM `{$table}`");
                while ($row = $stmt->fetch(PDO::FETCH_ASSOC)) {
                    $existingIndexes[$row['Key_name']] = true;
                }
            } catch (\PDOException $e) {
                continue;
            }

            // Create missing indexes
            foreach ($tableIndexes as $indexName => $columns) {
                if (isset($existingIndexes[$indexName])) {
                    continue; // Index already exists
                }

                try {
                    $pdo->exec("ALTER TABLE `{$table}` ADD INDEX `{$indexName}` ({$columns})");
                } catch (\PDOException $e) {
                    // Log error but continue
                    error_log("Index optimization warning ({$table}.{$indexName}): " . $e->getMessage());
                }
            }
        }

        return true;
    }

