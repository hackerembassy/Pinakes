    /**
     * Register and activate default plugins
     * Plugins: open-library, z39-server, api-book-scraper, digital-library, dewey-editor
     * Excluded: scraping-pro (commercial/proprietary)
     */
    public function installPluginsFromZip() {
        $pdo = $this->getDatabaseConnection();
        $results = [];

        // Helper to ensure hooks exist
        $ensureHooks = function(int $pluginId, array $hooks, string $callbackClass) use ($pdo) {
            $existing = $pdo->prepare("SELECT hook_name, callback_method FROM plugin_hooks WHERE plugin_id = ?");
            $existing->execute([$pluginId]);
            $existingMap = [];
            foreach ($existing->fetchAll(PDO::FETCH_ASSOC) as $row) {
                $existingMap[$row['hook_name'] . '|' . $row['callback_method']] = true;
            }

            foreach ($hooks as $hook) {
                $hookName = $hook['name'];
                if (empty($hook['callback_method'])) {
                    // Skip hooks without explicit callback_method — auto-generated names
                    // like "handleScrapefetchcustom" don't match real plugin methods
                    continue;
                }
                $callbackMethod = $hook['callback_method'];
                $priority = $hook['priority'] ?? 10;
                $key = $hookName . '|' . $callbackMethod;
                if (isset($existingMap[$key])) {
                    continue;
                }

                $stmt = $pdo->prepare("
                    INSERT INTO plugin_hooks (plugin_id, hook_name, callback_class, callback_method, priority, is_active, created_at)
                    VALUES (?, ?, ?, ?, ?, 1, NOW())
                ");
                $stmt->execute([$pluginId, $hookName, $callbackClass, $callbackMethod, $priority]);
            }
        };

        // Generic plugin installer
        $installPlugin = function(string $pluginName, array $additionalHooks = []) use ($pdo, $ensureHooks, &$results) {
            try {
                $pluginDir = $this->baseDir . '/storage/plugins/' . $pluginName;

                if (!is_dir($pluginDir)) {
                    $results[] = ['name' => $pluginName, 'status' => 'error', 'message' => 'Directory not found'];
                    return;
                }

                $pluginJsonPath = $pluginDir . '/plugin.json';
                if (!file_exists($pluginJsonPath)) {
                    $results[] = ['name' => $pluginName, 'status' => 'error', 'message' => 'plugin.json not found'];
                    return;
                }

                $pluginJson = json_decode(file_get_contents($pluginJsonPath), true);
                if (!$pluginJson) {
                    $results[] = ['name' => $pluginName, 'status' => 'error', 'message' => 'Invalid plugin.json'];
                    return;
                }

                $stmt = $pdo->prepare("SELECT id FROM plugins WHERE name = :name LIMIT 1");
                $stmt->execute(['name' => $pluginJson['name']]);
                $pluginId = (int)$stmt->fetchColumn();

                if (!$pluginId) {
                    $insertStmt = $pdo->prepare("
                        INSERT INTO plugins (name, display_name, description, version, author, author_url, plugin_url,
                            is_active, path, main_file, requires_php, requires_app, metadata, installed_at)
                        VALUES (:name, :display_name, :description, :version, :author, :author_url, :plugin_url,
                            1, :path, :main_file, :requires_php, :requires_app, :metadata, NOW())
                    ");

                    $insertStmt->execute([
                        'name' => $pluginJson['name'],
                        'display_name' => $pluginJson['display_name'],
                        'description' => $pluginJson['description'],
                        'version' => $pluginJson['version'],
                        'author' => $pluginJson['author'] ?? '',
                        'author_url' => $pluginJson['author_url'] ?? '',
                        'plugin_url' => $pluginJson['plugin_url'] ?? '',
                        'path' => $pluginName,
                        'main_file' => $pluginJson['main_file'],
                        'requires_php' => $pluginJson['requires_php'] ?? '8.0',
                        'requires_app' => $pluginJson['requires_app'] ?? '1.0',
                        'metadata' => json_encode($pluginJson['metadata'] ?? [])
                    ]);

                    $pluginId = (int)$pdo->lastInsertId();
                }

                // Register hooks from plugin.json
                $hooks = $pluginJson['metadata']['hooks'] ?? [];
                // Merge with additional hooks
                $hooks = array_merge($hooks, $additionalHooks);

                // Determine callback class from plugin name (CamelCase convention)
                $callbackClass = str_replace(' ', '', ucwords(str_replace('-', ' ', $pluginName))) . 'Plugin';

                if (!empty($hooks)) {
                    $ensureHooks($pluginId, $hooks, $callbackClass);
                }

                $results[] = ['name' => $pluginName, 'status' => 'installed_and_activated', 'plugin_id' => $pluginId];

            } catch (Exception $e) {
                $results[] = ['name' => $pluginName, 'status' => 'error', 'message' => $e->getMessage()];
            }
        };

        // NOTE: The 5 historically-default plugins plus mobile-api (active by default
        // since 0.7.21) are auto-activated on fresh install.
        // Other bundled plugins (BundledPlugins::LIST) are registered later by
        // autoRegisterBundledPlugins() and remain deactivated until admin opt-in.
        // Keeping new plugins opt-in is safer for fresh installs: it avoids surprising
        // behavior changes, side effects (extra routes, DB tables, background jobs),
        // and lets the admin review and enable each plugin consciously.
        // Install all default plugins (excluding scraping-pro)
        $installPlugin('open-library');
        $installPlugin('z39-server', [
            ['name' => 'app.routes.register', 'callback_method' => 'registerRoutes', 'priority' => 10],
            ['name' => 'scrape.fetch.custom', 'callback_method' => 'fetchBookMetadata', 'priority' => 3]
        ]);
        $installPlugin('api-book-scraper');
        $installPlugin('digital-library', [
            ['name' => 'app.routes.register', 'callback_method' => 'registerRoutes', 'priority' => 10]
        ]);
        $installPlugin('dewey-editor', [
            ['name' => 'app.routes.register', 'callback_method' => 'registerRoutes', 'priority' => 10]
        ]);
        // Mobile API: active by default since 0.7.21 so a fresh install ships a
        // working /api/v1 for the companion Android app out of the box. Its tables
        // are created by schema.sql (mirrors MobileApiPlugin::ensureSchema). Existing
        // installs get it shipped + registered but INACTIVE on upgrade — admin opts in.
        $installPlugin('mobile-api', [
            ['name' => 'app.routes.register', 'callback_method' => 'registerRoutes', 'priority' => 10],
            ['name' => 'mobile_api.dispatch_push', 'callback_method' => 'dispatchPush', 'priority' => 20]
        ]);

        // Configure Z39 Server with SBN (Servizio Bibliotecario Nazionale) as default
        $this->configureZ39DefaultSettings($pdo);

        return $results;
    }

    /**
     * Configure Z39 Server plugin with locale-appropriate default servers
     *
     * For Italian installations: Only SBN (JSON API) is enabled by default
     * For English installations: SBN + European SRU servers (K10plus, SUDOC, DNB)
     *
     * This prevents author name format inconsistencies when scraping from multiple
     * sources. SBN returns consistent "Surname, Name" format which gets normalized.
     * Foreign servers may return different formats that could cause duplicates.
     *
     * @param PDO $pdo Database connection
     */
    private function configureZ39DefaultSettings(PDO $pdo): void
    {
        try {
            // Get Z39 Server plugin ID
            $stmt = $pdo->prepare("SELECT id FROM plugins WHERE name = 'z39-server' LIMIT 1");
            $stmt->execute();
            $pluginId = (int)$stmt->fetchColumn();

            if (!$pluginId) {
                return; // Plugin not installed
            }

            // Check installation locale from session
            // Session value can be 'it' or 'en_US' (from step0.php form)
            // Use str_starts_with for robustness in case locale format changes
            $locale = $_SESSION['app_locale'] ?? 'it';
            $isItalian = str_starts_with($locale, 'it');

            // Default Z39.50/SRU servers configuration
            // Italian installations: Empty array - only SBN (JSON API) will be used
            // English installations: Include European SRU servers for broader coverage
            if ($isItalian) {
                // Italian: Only SBN (prevents author name format inconsistencies)
                $defaultServers = json_encode([]);
            } else {
                // English: Include European SRU servers
                // K10plus (Germany) is the most reliable European SRU server
                // SUDOC (France) is good for European books
                // DNB (Germany) has comprehensive German-language coverage
                $defaultServers = json_encode([
                    [
                        'name' => 'K10plus (GBV Germany)',
                        'url' => 'https://sru.k10plus.de/opac-de-627',
                        'database' => 'opac-de-627',
                        'syntax' => 'marcxml',
                        'enabled' => true,
                        'timeout' => 15,
                        'indexes' => ['isbn' => 'pica.isb']
                    ],
                    [
                        'name' => 'SUDOC (France)',
                        'url' => 'https://www.sudoc.abes.fr/cbs/sru/',
                        'database' => '',
                        'syntax' => 'unimarc',
                        'enabled' => true,
                        'timeout' => 15,
                        'indexes' => ['isbn' => 'isb']
                    ],
                    [
                        'name' => 'DNB - Deutsche Nationalbibliothek',
                        'url' => 'https://services.dnb.de/sru/dnb',
                        'database' => 'dnb',
                        'syntax' => 'marcxml',
                        'enabled' => true,
                        'timeout' => 15,
                        'indexes' => ['isbn' => 'num']
                    ]
                ]);
            }

            // Default settings for Z39 Server
            $defaultSettings = [
                'enable_client' => '1',           // Enable Z39.50 client for scraping
                'servers' => $defaultServers,     // Empty for IT, K10plus+SUDOC+DNB for EN
                'server_enabled' => '1',          // Enable SRU server mode
                'enable_server' => '1',
                'max_records' => '100',
                'default_records' => '10',
                'supported_formats' => 'marcxml,dc,mods,oai_dc',
                'default_format' => 'marcxml',
                'rate_limit_enabled' => '1',
                'rate_limit_requests' => '100',
                'rate_limit_window' => '3600',
                'enable_logging' => '1',
                'cql_version' => '1.2',
                'sru_version' => '1.2',
                // SBN (Italian National Library) - enabled by default for ALL locales
                // SBN provides excellent coverage for Italian books and many international titles
                'enable_sbn' => '1',              // Enable SBN JSON API client
                'sbn_timeout' => '15'             // SBN request timeout in seconds
            ];

            // Insert settings (skip if already exist)
            $insertStmt = $pdo->prepare("
                INSERT IGNORE INTO plugin_settings (plugin_id, setting_key, setting_value, autoload, created_at)
                VALUES (:plugin_id, :key, :value, 1, NOW())
            ");

            foreach ($defaultSettings as $key => $value) {
                $insertStmt->execute([
                    'plugin_id' => $pluginId,
                    'key' => $key,
                    'value' => $value
                ]);
            }

        } catch (Exception $e) {
            // Log but don't fail installation
            error_log('[Installer] Failed to configure Z39 defaults: ' . $e->getMessage());
        }
    }

    /**
     * Set secure file permissions after installation
     * - Directories: 755 (rwxr-xr-x)
     * - Files: 644 (rw-r--r--)
     * - Writable directories (storage, logs): 775
     * - Sensitive files (.env): 600
     *
     * @return array Results with counts of modified items
     */
    public function setSecurePermissions(): array
    {
        $results = [
            'directories' => 0,
            'files' => 0,
            'writable_dirs' => 0,
            'sensitive_files' => 0,
            'errors' => []
        ];

        // Directories that need write permissions (775)
        $writableDirs = [
            'storage',
            'storage/cache',
            'storage/logs',
            'storage/plugins',
            'storage/uploads',
            'storage/uploads/plugins',
            'storage/backups',
            'storage/tmp',
            'storage/sessions',
            'cache',
            'tmp',
            'public/assets',
            'public/uploads',
            'public/uploads/autori',
        ];

        // Sensitive files that need restricted permissions (600)
        $sensitiveFiles = [
            '.env',
            '.installed',
        ];

        // Directories to skip (vendor, node_modules, .git)
        $skipDirs = ['vendor', 'node_modules', '.git', 'installer'];

        // First, handle sensitive files
        foreach ($sensitiveFiles as $file) {
            $path = $this->baseDir . '/' . $file;
            if (file_exists($path)) {
                if (@chmod($path, 0600)) {
                    $results['sensitive_files']++;
                } else {
                    $results['errors'][] = "Cannot set permissions on: $file";
                }
            }
        }

        // Handle writable directories first (create if missing)
        foreach ($writableDirs as $dir) {
            $path = $this->baseDir . '/' . $dir;
            if (!is_dir($path)) {
                @mkdir($path, 0775, true);
            }
            if (is_dir($path)) {
                if (@chmod($path, 0775)) {
                    $results['writable_dirs']++;
                } else {
                    $results['errors'][] = "Cannot set permissions on directory: $dir";
                }
            }
        }

        // Recursively set permissions on all other files and directories
        $this->setPermissionsRecursive(
            $this->baseDir,
            $skipDirs,
            $writableDirs,
            $sensitiveFiles,
            $results
        );

        return $results;
    }

    /**
     * Recursively set permissions on files and directories
     */
    private function setPermissionsRecursive(
        string $dir,
        array $skipDirs,
        array $writableDirs,
        array $sensitiveFiles,
        array &$results,
        string $relativePath = ''
    ): void {
        if (!is_dir($dir)) {
            return;
        }

        $items = @scandir($dir);
        if ($items === false) {
            return;
        }

        foreach ($items as $item) {
            if ($item === '.' || $item === '..') {
                continue;
            }

            $path = $dir . '/' . $item;
            $relPath = $relativePath ? $relativePath . '/' . $item : $item;

            // Skip certain directories
            if (is_dir($path) && in_array($item, $skipDirs)) {
                continue;
            }

            // Skip files/dirs we already handled
            if (in_array($relPath, $sensitiveFiles)) {
                continue;
            }

            // Check if this is a writable directory (or inside one)
            $isWritableDir = false;
            foreach ($writableDirs as $wDir) {
                if ($relPath === $wDir || strpos($relPath, $wDir . '/') === 0) {
                    $isWritableDir = true;
                    break;
                }
            }

            if (is_dir($path)) {
                // Set directory permissions
                $targetPerm = $isWritableDir ? 0775 : 0755;
                if (@chmod($path, $targetPerm)) {
                    $results['directories']++;
                }

                // Recurse into subdirectory
                $this->setPermissionsRecursive(
                    $path,
                    $skipDirs,
                    $writableDirs,
                    $sensitiveFiles,
                    $results,
                    $relPath
                );
            } else {
                // Set file permissions (644 for normal, 664 for files in writable dirs)
                $targetPerm = $isWritableDir ? 0664 : 0644;
                if (@chmod($path, $targetPerm)) {
                    $results['files']++;
                }
            }
        }
    }

    /**
     * Delete installer directory for security
     */
    public function deleteInstaller() {
        $installerDir = dirname(__DIR__);
        return $this->recursiveDelete($installerDir);
    }

    /**
     * Recursively delete directory
     */
    private function recursiveDelete($dir) {
        if (!is_dir($dir)) {
            return false;
        }

        $files = array_diff(scandir($dir), ['.', '..']);

        foreach ($files as $file) {
            $path = $dir . '/' . $file;
            is_dir($path) ? $this->recursiveDelete($path) : unlink($path);
        }

        return rmdir($dir);
    }
